#[cfg(test)]
mod tests {
    use super::PATCH;

    // Tripwire against re-introducing the degenerate A001 "Lead Patch" link-prover
    // as the boot default (it renders as a bare square wave: no envelope, no filter
    // routing — see the symptom that motivated this file). The first 16 bytes are
    // the patch's ASCII name field; A001 is "Lead Patch", A052 is "Nice Lead".
    #[test]
    fn boot_patch_is_not_the_degenerate_link_prover() {
        let name = &PATCH[..16];
        assert_ne!(name, b"Lead Patch      ", "boot patch reverted to degenerate A001");
        assert_eq!(name, b"Nice Lead       ", "unexpected boot patch name");
    }
}
